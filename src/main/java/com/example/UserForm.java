package com.example;

public class UserForm {
    private String name;
    private String nm_add;
    private String nk_tel;
    private String birthday;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
	public String getNm_add() {
		return nm_add;
	}
	public void setNm_add(String nm_add) {
		this.nm_add = nm_add;
	}
	public String getNk_tel() {
		return nk_tel;
	}
	public void setNk_tel(String nk_tel) {
		this.nk_tel = nk_tel;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
}
